class Graph:
    """Represent a graph as a dictionary of vertices mapping labels to edges."""
    def __init__(self):
        self.vertices = dict()
    def add_vertex(self, vertex):
        """
        Add a vertex to the graph.
        """
        if vertex not in self.vertices:
            self.vertices[vertex] = set()
        else:
            print("Warning, vertex exists")

    def add_edge(self, v_from, v_to):
        """
        Add a directed edge to the graph.
        """
        if v_from in self.vertices and v_to in self.vertices:
            self.vertices[v_from].add(v_to)

    def bft(self, starting_vertex):
        """
        Print each vertex in breadth-first order
        beginning from starting_vertex.
        """
        q = Queue()

        q.enqueue(starting_vertex)

        found = set([starting_vertex])
        found_order = [starting_vertex, ]

        while q.size() > 0:
            for vertex in self.vertices[q.queue[0]]:
                if vertex not in found:
                    q.enqueue(vertex)
                    found.add(vertex)
                    found_order.append(vertex)
            q.dequeue()

        print(f'BFT {found}')

    def dft(self, starting_vertex):
        """
        Print each vertex in depth-first order
        beginning from starting_vertex.
        """
        s = Stack()

        s.push(starting_vertex)

        found = set([starting_vertex])
        found_order = [starting_vertex, ]

        while s.size() > 0:
            v_found = False
            for vertex in self.vertices[s.stack[-1]]:
                if vertex not in found:
                    v_found = True
                    s.push(vertex)
                    found.add(vertex)
                    found_order.append(vertex)
                    break
            if not v_found:
                s.pop()

        print(f'DFT {found_order}')

